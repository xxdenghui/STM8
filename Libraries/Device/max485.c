/**
  * @brief  rs485 send buff
  * values.
  * @param  buff, lenth
  * @retval None
  */
void rs485_send( uint8_t* buff, uint16_t lenth )
{
  RS485_Tx();
  uart_send( uart1, buff, lenth );
}

/**
  * @brief  rs485 configuration
  * values.
  * @param  none
  * @retval None
  */
void rs485_cfg( void )
{
  uart_cfg( uart1, Baud_9600, dat8, stb1, no, no_it );
}
  
